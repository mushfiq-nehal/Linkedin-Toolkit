export const RECENT_EMOJI_STORAGE_KEY = 'linkedin-toolkit:recent-emojis';

export function loadRecentEmojis(): string[] {
  try {
    const raw = localStorage.getItem(RECENT_EMOJI_STORAGE_KEY);
    if (!raw) return [];
    return JSON.parse(raw) as string[];
  } catch {
    return [];
  }
}

export function saveRecentEmoji(emoji: string, current: string[]): string[] {
  const updated = [emoji, ...current.filter(e => e !== emoji)].slice(0, 24);
  try {
    localStorage.setItem(RECENT_EMOJI_STORAGE_KEY, JSON.stringify(updated));
  } catch {
    // ignore storage errors
  }
  return updated;
}
